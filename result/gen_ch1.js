
const fs = require("fs");
const path = require("path");
const { Packer } = require("docx");
const {
  PAGE_PROPERTIES,
  STYLES,
  Document,
  createH1,
  createH2,
  createH3,
  createBodyParagraph,
} = require("./docx_utils");

async function main() {
  const children = [];

  // ── Title ──
  children.push(createH1("1 绪论"));

  // ── 1.1 研究背景与意义 ──
  children.push(createH2("1.1 研究背景与意义"));

  children.push(createBodyParagraph(
    "近年来，随着智能制造、智慧物流以及服务机器人等技术的持续推进，移动机器人已被广泛应用于仓储配送、医疗辅助、工业巡检等诸多行业领域。在上述应用场景中，路径规划是移动机器人系统的核心环节之一，其优劣直接关系到任务执行效率、能量消耗水平及运行安全性等关键性能指标[30][2][12][43][39]。如何在结构复杂且存在多种约束条件的作业环境中规划出兼具高效性与平滑性的行进路径，始终是该领域亟需深入研究的关键问题。"
  ));

  children.push(createBodyParagraph(
    "目前，路径规划算法大体可分为两类：一类是以A*算法和Dijkstra算法为代表的确定性搜索方法，另一类是以遗传算法（GA）为代表的智能优化方法。前者在理论上能够保证找到最优或可行路径，但在工程实践中暴露出若干不足。以A*算法为例，当环境中障碍物分布密集时，该算法需逐一扩展大量候选节点，计算代价随地图规模增大而急剧上升；其启发函数权重固定不变，无法依据局部环境复杂度做出适应性调整；此外，受栅格离散化与搜索策略本身的制约，生成的路径往往包含过多冗余转折，不利于机器人的实际执行。遗传算法虽然在全局寻优方面具备较强的鲁棒性，但其标准形式同样面临收敛速度偏慢、易于陷入局部最优以及关键参数高度依赖人工经验等问题，难以在搜索效率与解质量之间取得令人满意的平衡[13][22][3][8][11][45][46]。"
  ));

  children.push(createBodyParagraph(
    "上述分析表明，单一类型的算法已难以兼顾搜索效率、路径质量和环境自适应性的多重需求。鉴于此，本文提出一种将改进A*算法与自适应遗传算法相融合的路径规划方法。该方法的核心思路在于：首先在A*算法中引入基于障碍物密度的动态权重因子，依据环境复杂程度自适应地调节启发式权重，以兼顾搜索效率与路径最优性；随后将改进A*生成的高质量路径作为遗传算法的初始种群，借助自适应交叉与变异策略在全局解空间中进一步优化路径长度、平滑度等多项指标；最后配合专门设计的路径修复策略保障输出路径的连通性与可行性。本研究旨在为移动机器人在复杂栅格环境下的自主导航提供一种更为高效、可靠的规划方案，具有一定的理论意义和应用价值。"
  ));

  // ── 1.2 国内外研究现状 ──
  children.push(createH2("1.2 国内外研究现状"));

  // ── 1.2.1 传统路径规划算法研究 ──
  children.push(createH3("1.2.1 传统路径规划算法研究"));

  children.push(createBodyParagraph(
    `A*算法由Hart等于1968年首次提出[23]，是启发式图搜索领域的经典方法。该算法通过评估函数 $f(n) = g(n) + h(n)$ 选择待扩展节点，其中 $g(n)$ 表示起点至当前节点 $n$ 的已知累积代价，$h(n)$ 为节点 $n$ 至目标点的启发式估计代价。当启发函数满足可容许性条件时，A*算法能够保证找到代价最小的路径[12][13][32]。然而，经典A*算法采用固定权重的启发函数，当环境中障碍物分布密集时，算法需扩展大量节点以绕过死胡同和凹形障碍区域，搜索效率随之显著下降。针对这一问题，国内外学者提出了多种改进方案，包括动态权重调整[33]、多目标优化[34]、以及结合拓扑地图的加速策略[50]等。`
  ));

  children.push(createBodyParagraph(
    "Dijkstra算法是另一种经典的最短路径求解方法，由Dijkstra于1959年提出[24]。该算法以均匀代价搜索策略遍历图中节点，能够精确求得任意两点间的最短路径；但由于不引入任何启发式信息，其搜索范围通常覆盖大片区域，在大规模栅格地图中计算开销显著[12][41]。"
  ));

  children.push(createBodyParagraph(
    `Jump Point Search（JPS）算法由Harabor和Grastien于2011年提出[25]，是一种面向均匀代价网格的加速搜索技术。JPS利用路径对称性实施节点剪枝，仅对关键的"跳点"进行扩展，在规则网格地图上可将搜索效率提升至传统A*算法的数倍乃至数十倍。然而，JPS的优化目标仅限于路径长度，未涉及平滑度等其他质量维度，且其跳跃式的路径点分布往往导致路径转折剧烈[12][39]。`
  ));

  // ── 1.2.2 智能优化算法在路径规划中的应用 ──
  children.push(createH3("1.2.2 智能优化算法在路径规划中的应用"));

  children.push(createBodyParagraph(
    "遗传算法（Genetic Algorithm, GA）由Holland于1975年系统阐述[26]，是一种模拟自然选择与遗传机制的全局随机优化方法。GA通过选择、交叉、变异等遗传算子在解空间中进行并行搜索，在求解非线性、多峰及组合优化问题方面表现出较强的鲁棒性[2][45]。近年来，GA在路径规划领域得到了广泛应用，涵盖多目标路径优化、动态环境下的路径重规划等研究方向[9][40]。"
  ));

  children.push(createBodyParagraph(
    "然而，标准GA应用于路径规划时存在若干固有缺陷：其一，初始种群通常随机生成，缺乏来自问题结构的先验引导，导致算法早期搜索效率低下；其二，交叉率和变异率在进化全过程中保持恒定，无法对不同质量的个体实施差异化操作——高适应度个体的优质基因片段容易在交叉中被破坏，而低适应度个体又难以获得足够强度的变异改进；其三，随着进化推进种群多样性逐步丧失，算法容易陷入局部最优（即早熟收敛现象）；其四，路径规划中的障碍物约束属于硬约束，标准GA对交叉变异产生的不可行路径缺乏有效的处理手段。针对上述问题，国内外学者先后提出了自适应参数调整机制[4][18][46]、精英保留策略[5][48]、停滞打破机制[47][49]以及多种混合算法架构[14]等改进方案。"
  ));

  // ── 1.2.3 融合算法研究现状 ──
  children.push(createH3("1.2.3 融合算法研究现状"));

  children.push(createBodyParagraph(
    "将不同类型算法的互补优势加以融合，已逐渐成为路径规划领域的重要研究方向。现有融合策略大致可归纳为三类：第一类是串行级联式，即先利用计算效率较高的确定性搜索算法生成初始可行路径，再以智能优化算法对其进行二次优化；第二类是约束引导式，将一种算法的输出作为另一种算法的初始条件或搜索约束；第三类是并行协作式，多种算法同时运行并共享搜索信息。已有研究表明，经过合理设计的融合方案能够在搜索效率与路径质量之间取得较好的折中，在复杂环境中优势尤为突出[1][6][35][36]。"
  ));

  children.push(createBodyParagraph(
    "就A*算法与遗传算法的融合而言，虽然已有部分探索性工作，但现有方案仍存在以下不足：第一，多数研究直接采用标准A*算法生成初始路径，未充分利用环境特征信息来提升初始解质量，导致后续优化空间受限；第二，遗传算法中交叉率和变异率的自适应策略设计不够精细，未能根据个体适应度水平实施差异化调节，制约了种群的进化效率；第三，对遗传操作过程中产生的不可行路径缺乏系统的修复机制，通常仅采取简单丢弃策略，造成了计算资源的浪费并降低了有效种群规模[8][15][4][9][14][1][6][37][38]。"
  ));

  // ── 1.3 本文主要研究内容 ──
  children.push(createH2("1.3 本文主要研究内容"));

  children.push(createBodyParagraph(
    "针对上述问题，本文围绕融合改进A*算法与自适应遗传算法的移动机器人路径规划方法展开研究，主要工作包括以下四个方面："
  ));

  children.push(createBodyParagraph(
    "（1）改进A*算法设计。引入基于障碍物密度的动态权重因子 $\\alpha(\\rho)$，建立环境复杂度与启发函数权重之间的映射关系，使算法在低密度区域维持路径最优性，在高密度区域增强搜索引导力度，从而有效提升搜索效率。"
  ));

  children.push(createBodyParagraph(
    "（2）自适应遗传算法设计。利用不同动态权重参数的改进A*生成多条差异化路径构成初始种群，提高初始解的质量与多样性；设计综合考虑路径长度、平滑度、转折点数量和碰撞惩罚的多目标适应度函数；提出基于个体适应度的自适应交叉率与变异率调整策略，并引入精英保留机制和停滞打破机制以防止早熟收敛。"
  ));

  children.push(createBodyParagraph(
    `（3）路径修复机制设计。针对遗传操作产生的不可行路径，提出\u201cA*重连优先、替代点搜索备选\u201d的两级递归修复策略，优先在非法路径段的前后合法节点间执行局部A*搜索实现重连，若重连失败则在非法点邻域内搜索满足视线约束的替代点，以确保输出路径的连通性和可行性。`
  ));

  children.push(createBodyParagraph(
    "（4）系统实验验证与分析。在1500×1500的大规模栅格地图上设计五阶段对比实验，将所提融合方案分别与Standard A*、Dijkstra、JPS及JPS+GA等多种基准算法进行系统比较，从路径长度、平滑度、转折点数量、计算效率和路径有效性等多个维度全面评估算法性能。"
  ));

  // ── 1.4 本章小结 ──
  children.push(createH2("1.4 本章小结"));

  children.push(createBodyParagraph(
    "本章首先阐述了移动机器人路径规划问题的研究背景与实际意义，分析了路径规划在智能制造、智慧物流等领域的核心地位。随后，系统梳理了传统路径规划算法（A*、Dijkstra、JPS）和智能优化算法（遗传算法）的研究现状，指出了各类算法的优势与局限性。在此基础上，分析了现有融合算法研究的不足之处，明确了本文的研究切入点。最后，概述了本文的主要研究内容，包括改进A*算法设计、自适应遗传算法设计、路径修复机制设计以及系统实验验证四个方面，为后续章节的展开奠定了基础。"
  ));

  // ── Build document ──
  const doc = new Document({
    ...PAGE_PROPERTIES,
    styles: STYLES,
    sections: [
      {
        properties: PAGE_PROPERTIES.page ? { page: PAGE_PROPERTIES.page } : {},
        children,
      },
    ],
  });

  const buffer = await Packer.toBuffer(doc);
  const outPath = path.join(__dirname, "adjust", "第一章.docx");
  try { fs.unlinkSync(outPath); } catch (e) {}
  fs.writeFileSync(outPath, buffer);
  console.log("✅ Generated:", outPath);
}

main().catch((err) => {
  console.error("❌ Error:", err);
  process.exit(1);
});
